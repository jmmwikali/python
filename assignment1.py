# 1. Write a python program that is able to calculate the BMI of a person whose weight is 75kgs and height is 1.75 meters. BMI = (weight) / (height squared).
weight = 75
height = 1.75
BMI = (weight) / (height ** 2)
print("This is the BMI of the person:", BMI)

# 2. Find the Area and perimeter of a rectangle whose length is 48cm and width is 25cm

# Area
length = 48
width = 25
area = length * width
print("The Area of the rectangle is:", area)

# Perimeter
perimeter = 2 * (length + width)
print("The Perimeter of the rectangle is:", perimeter,)


