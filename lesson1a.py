# What is a variable: Variables are labels which act as containers to hold values. in python, a variable is always assiciated with some values. 
# We use the assignment operator (=) to assign values to a variable. 
# Anything that is inside of the quotes in python is called a string
message = "Hello there! Hope everything is fine?"
print(message)

message2 = " Goodbye"
print(message2)

# Concatinating variables (Joining two or more outputs together)
message3 = message + message2
print(message3)

# Variables can be named  using a letter, a number and an underscore. They can start with a letter and an underscore but not with a number.
_name1 = "Mwikali"
print(_name1)

# Variable names cannot contain spaces. We can use an underscore to separate the names.
total_marks = 85
print(total_marks)

